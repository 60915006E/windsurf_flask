-- ===========================================
-- 動態主題館管理系統 - 資料庫表結構
-- ===========================================

-- 專題館設定表
CREATE TABLE COLLECTION_CONFIG (
    COLLECTION_ID VARCHAR2(50) PRIMARY KEY,        -- 專題館 ID
    COLLECTION_NAME VARCHAR2(200) NOT NULL,       -- 專題館名稱
    DESCRIPTION VARCHAR2(1000),                  -- 專題館描述
    SQL_FILTER_CONDITION CLOB,                   -- SQL 篩選條件
    SCHEDULE_TYPE VARCHAR2(20) DEFAULT 'DAILY',  -- 排程類型 (HOURLY, DAILY, WEEKLY, MONTHLY)
    SCHEDULE_INTERVAL NUMBER DEFAULT 1,          -- 排程間隔
    LAST_RUN_TIME DATE,                          -- 最後執行時間
    NEXT_RUN_TIME DATE,                          -- 下次執行時間
    IS_ACTIVE CHAR(1) DEFAULT 'Y',               -- 是否啟用
    AUTO_UPDATE CHAR(1) DEFAULT 'Y',             -- 是否自動更新
    CREATED_DATE DATE DEFAULT SYSDATE,           -- 建立日期
    UPDATED_DATE DATE DEFAULT SYSDATE,           -- 更新日期
    CREATED_BY VARCHAR2(50),                     -- 建立者
    UPDATED_BY VARCHAR2(50)                     -- 更新者
);

-- 專題館項目表
CREATE TABLE COLLECTION_ITEMS (
    ITEM_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,  -- 項目 ID
    COLLECTION_ID VARCHAR2(50) NOT NULL,         -- 專題館 ID
    OVC_RP_NO VARCHAR2(50) NOT NULL,             -- 報告編號
    ADDED_DATE DATE DEFAULT SYSDATE,             -- 加入日期
    ADDED_BY VARCHAR2(50),                      -- 加入者
    ADD_TYPE VARCHAR2(20) DEFAULT 'AUTO',        -- 加入類型 (AUTO, MANUAL)
    IS_ACTIVE CHAR(1) DEFAULT 'Y',               -- 是否啟用
    NOTES VARCHAR2(500),                         -- 備註
    CREATED_DATE DATE DEFAULT SYSDATE,           -- 建立日期
    UPDATED_DATE DATE DEFAULT SYSDATE,           -- 更新日期
    UPDATED_BY VARCHAR2(50),                     -- 更新者
    CONSTRAINT FK_COLLECTION_ITEMS_COLLECTION FOREIGN KEY (COLLECTION_ID) 
        REFERENCES COLLECTION_CONFIG(COLLECTION_ID) ON DELETE CASCADE
);

-- 建立索引
CREATE INDEX IDX_COLLECTION_CONFIG_ACTIVE ON COLLECTION_CONFIG(IS_ACTIVE, AUTO_UPDATE);
CREATE INDEX IDX_COLLECTION_CONFIG_SCHEDULE ON COLLECTION_CONFIG(NEXT_RUN_TIME, IS_ACTIVE);
CREATE INDEX IDX_COLLECTION_ITEMS_COLLECTION ON COLLECTION_ITEMS(COLLECTION_ID, IS_ACTIVE);
CREATE INDEX IDX_COLLECTION_ITEMS_RP_NO ON COLLECTION_ITEMS(OVC_RP_NO);
CREATE UNIQUE INDEX IDX_COLLECTION_ITEMS_UNIQUE ON COLLECTION_ITEMS(COLLECTION_ID, OVC_RP_NO) WHERE IS_ACTIVE = 'Y';

